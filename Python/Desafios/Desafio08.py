m = float(input('Uma distancia em metros:'))
km = m / 1000
hm = m / 100
dam = m / 10
dm = m * 10
cm = m * 100
mm = m * 1000

print('As medidas de comprimento desse número são:')
print(f'Em km é: {km}km')
print(f'Em hm é: {hm}hm')
print(f'Em dam é: {dam}dam')
print(f'Em dm é: {dm}dm')
print(f'Em cm é: {cm}cm')
print(f'Em mm é: {mm}mm')